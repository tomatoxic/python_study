# 파이썬 기본 데이터 타입
a = 7
b = 7.777
c = True
d = False
e = '문자열'

print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(2/4)
print(type(2/4))

# 제곱과 나누기
print(2**3) # 2*2*2 
print(5//2) # 몫 2
print(5%2) # 나머지 1

# 반올림 round(숫자, 자리수)
print(round(3.156, 2)) # 소수점 둘째자리까지 반올림

# 절대값 abs()
print(abs(-20))